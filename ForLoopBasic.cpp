
// Print Number 1 to 50;

#include<iostream>
using namespace std;
int main()
{
    //Initialization  // Condition check     // Update expression  
for(int counter=1;    counter<=5;          counter++)
{
	// Body of Loop 
	cout<<counter<<endl;                
}

//First iteration
// counter =1 , 1<=5,execute the body ,counter++ (1+1) 
// 2<=5,  execute the body, counter++ (2+1)
// 3<=5 ,execute the body, counter++ (3+1)
// 4<=5, execute the body, counter++ (4+1)
//5 <=5, execute the Body, counter++ (5+1)
//6 <=5, Dont execute the Body, Terminate the Loop 

cout<<"After For Loop";

return 0;
}
